uint8_t  MTx_DriverEnable(uint8_t chIndex, enum eFlag bAble)
{
    // check
    if(chIndex >= MTx_NUM)
    {
		return e_Feedback_Error;
	}
	//
    if(e_False == bAble)
    {
        MTx_OUT_DRIVER_LOCK(chIndex);
		MTx_TIMER_COUNT_OFF(chIndex);
	}
	else
	{
        MTx_OUT_DRIVER_UNLOCK(chIndex);  // free
        MTx_TIMER_COUNT_ON(chIndex);
	}
	MTx_OUT_PULSE_LOW(chIndex);
		
    return e_Feedback_Success;
}

